package com.javaex.practice;

public class Ex14 {

	/*
	
	아래 프로그램의 결과를 코드작성전에 먼저 예상해보고 코드를 작성해 확인해 보세요

	 */
	
	public static void main(String[]srgs) {
		
		int a = 5;
		
		System.out.println(a); //5
		System.out.println(++a); //6
		System.out.println(a); //6
		System.out.println(a++); //6
		System.out.println(a); //7
		System.out.println(--a); //6
		System.out.println(a); //6
		System.out.println(a--); //6
		System.out.println(a); //5		
		
	}
	
	
}
